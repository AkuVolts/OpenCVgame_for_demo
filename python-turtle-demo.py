import math
import turtle
import time

# Set up the screen
turtle.setup(800, 600)
turtle.title("Python Turtle Graphics Demo")

# Set up the turtle
turtle.shape("turtle")
turtle.speed(0)
turtle.bgcolor("white")
turtle.color("black")
turtle.pensize(1)

# make the turtle
turtle = turtle.Turtle()

turtle.forward(100)
turtle.right(90)
turtle.color("red")
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)


time.sleep(20)



